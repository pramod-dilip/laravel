<div class="heading">Early Bloom of Rhododendrons in Himalayas Linked to Climate Change, Scientists Say</div>
<div class="col-sm-12" style="text-align:justify">
The rhododendron, one of the most popular and colorful small evergreen trees grown in gardens and parks across the world, is changing its flowering season from spring to winter in its native Himalayan habitat.<br /><br />

The blooming of rhododendron, with their conspicuous displays of deep red or pale pink flowers, has always heralded the arrival of spring in the Himalayas, but this has undergone a dramatic change. Peak flowering season is now early February to mid-March, instead of the spring months from March to May.<br /><br />

A group of scientists at the GB Pant Institute of Himalayan Environment and Sustainable Development in Almora, India, studied the change in flowering times of the native Rhododendron arboreum to see if they could relate it to climate change.<br /><br />
<b>Temperature Data</b><br /><br />
They report in Research Communications journal that they analyzed the long-term temperature data of the region for the previous 40 years (1971-2011) and found a comparatively small but significant increase in mean maximum temperature of 0.5 C over the period.<br /><br />
Using real time observations in three seasons, 2009 to 2011, and long-term herbarium records from 1893 to 2003, the scientists concluded that the peak flowering of the tree had moved forward three months—by between 88 and 97 days, depending on the recording site.
This was the most dramatic advance in flowering times of local plants studied. Another species, Aconitum heterophyllum, is highly prized for its tubers for use in Indian medicine—for example, for cough remedies and to treat diarrhea and indigestion. It occupies much of the same terrain, but has advanced its flowering period by only 17-25 days over the same period.<br /><br />
Dr. Ranbeer S. Rawal, who led the research, said alterations in the phenological activity of organisms had been shown by science to be the result of unusually warm temperatures, caused by human-induced climate change.<br /><br />
Phenology is the study of the times of recurring natural events, especially in relation to climate. The phenological responses of plants, particularly early flowering, are considered important biological indicators of climate change and the rhododendron results were among the most dramatic.<br /><br />

<b>Himalayan Range</b><br /><br />
"The present field observations over three years revealed a considerably higher frequency (47 percent - 75 percent trees) of bloom during February - March, which provides a strong basis to prove other observational reports of advancement in flowering events of target species from spring to winter," Dr. Rawal said.<br /><br />
The report concludes that the study's findings provide an important insight into species response to climate change in India's central Himalayan mountain range. It highlights the need for further research on the subject to improve our understanding of the effects of climate change on species and consequently on the ecology of the region.<br /><br />
"The trends definitely warrant attention as an indicator of climate change impacts on the flowering phenology of prominent regional species and the likely consequences for ecosystem processes," Dr. Rawal added.

