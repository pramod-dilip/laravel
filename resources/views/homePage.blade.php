<div class="col-sm-12" style="text-align:justify">The Mountain Cities Network (MCN) is a network to promote exchange of information, experience and best practices on urban development and city governance in Indian mountain regions and foster collaboration towards sustainable development of mountain cities. <br><br>

In order to achieve its objective, MCN provides platform for knowledge resources and multi-stakeholder dialogue and covers thematic focus ranging from urban development policies and governance, municipal finance, rural-urban interdependencies, mobility and public transport, energy, water and waste management,  climate change & DRR and human & institutional capacity development.<br><br>

The easy accessibility and circulation of the synthesized knowledge and information among the stakeholders engaged in the work related to the issues of urbanization in the Himalayas is a key requirement. The web portal as part for this network will make available resources to its target groups and stakeholders. The portal will works as a knowledge repository with compilation of useful knowledge and information available from various sources. Such a data-information-knowledge repository will address the long standing challenge of low accessibility to data and information from the Himalayas for the stakeholders including researchers and practitioners.

